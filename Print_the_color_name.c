#include<stdio.h>
int main()
{
    char co;
    scanf("%c",&co);
    if(co == 'V')
    printf("Violet");
    else if(co == 'I')
    printf("Indigo");
    else if(co == 'B')
    printf("Blue");
    else if(co == 'G')
    printf("Green");
    else if(co == 'Y')
    printf("Yellow");
    else if(co == 'O')
    printf("Orange");
    else if(co == 'R')
    printf("Red");
    else
    printf("-1");
    
}